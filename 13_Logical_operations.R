num1=as.numeric(readline("Enter the first number ::>> "))
num2=as.numeric(readline("Enter the second number ::>> "))


cat("Bitwise AND of",num1,"and",num2,"is ::>> ",num1 & num2)

cat("\nBitwise OR of",num1,"and",num2,"is ::>> ",num1 | num2)

cat("\nLogical OR of",num1,"and",num2,"is ::>> ",num1 || num2)

cat("\nLogical AND of",num1,"and",num2,"is ::>> ",num1 && num2)

cat("\nNegation of",num1,"is ::>> ",!num2)

cat("\n")
