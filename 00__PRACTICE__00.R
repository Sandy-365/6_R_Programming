#                 Operators
#=====================================================
#      Arithmetic operators------------->

# addition +
v <- c(2,5.5,6)
t <- c(8,3,4)
print(v+t)

# substraction -
v <- c(2,5.5,6)
t <- c(8,3,4)
print(v-t)

# multiplication *
v <- c(2,5.5,6)
t <- c(8,3,4)
print(v*t)

# division /
v <- c(2,5.5,6)
t <- c(8,3,4)
print(v/t)

# remainder %%
v <- c(2,5.5,6)
t <- c(8,3,4)
print(v%%t)

# Quotient %/%
v <- c(2,5.5,6)
t <- c(8,3,4)
print(v%/%t)

# power ^
v <- c(2,5.5,6)
t <- c(8,3,4)
print(v^t)

