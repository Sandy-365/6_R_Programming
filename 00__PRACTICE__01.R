#                 Operators
#=====================================================
#      Relational operators------------->

# Greater than >
v <- c(2,5.5,6)
t <- c(8,2,12)
print(v>t)

# Less than <
v <- c(2,5.5,6)
t <- c(8,2,12)
print(v<t)

# Equality ==
v <- c(2,5.5,6)
t <- c(2,5,6)
print(v==t)

# Not Equal != 
v <- c(2,5.5,6)
t <- c(8,2,12)
print(v!=t)

# Less than or Equal to <=
v <- c(2,5.5,6)
t <- c(2,5,6)
print(v<=t)

# Greater than or Equal to >=
v <- c(2,5.5,6)
t <- c(2,5,6)
print(v>=t)
